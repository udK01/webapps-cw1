class HomeController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $posts = Post::get();
        return view('home.index', [
            'posts' => DB::table('posts')->paginate(10)
        ]);
        //['posts' => $posts]);
